// FUNCTION - CHANGE BODY TEMA

function changeBodyTheme(){
    let obj=document.getElementById('body');
    if(obj.className=='bodyDark'){
        obj.className='bodyLight';
    }else{
        obj.className='bodyDark';
     }
    }

